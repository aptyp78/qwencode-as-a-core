def enrich_graph_with_timeline(graph_path: str, timeline: dict, output_path: str):
    """Обогащает граф данными временной шкалы."""
    
    # Загружаем граф
    with open(graph_path, "r", encoding="utf-8") as f:
        graph = json.load(f)
    
    # Добавляем временную шкалу в метаданные
    graph["metadata"]["timeline"] = {
        "total_events": len(timeline["events"]),
        "date_range": {
            "start": min(e["year"] for e in timeline["events"]),
            "end": max(e["year"] for e in timeline["events"])
        },
        "events_by_type": {}
    }
    
    # Подсчитываем события по типам
    for event in timeline["events"]:
        event_type = event["type"]
        graph["metadata"]["timeline"]["events_by_type"][event_type] = \
            graph["metadata"]["timeline"]["events_by_type"].get(event_type, 0) + 1
    
    # Добавляем события как узлы
    for i, event in enumerate(timeline["events"]):
        event_id = f"timeline_event_{i}"
        
        node = {
            "id": event_id,
            "type": "TimelineEvent",
            "name": event["title"],
            "attributes": {
                "year": event["year"],
                "type": event["type"],
                "description": event.get("description", ""),
                "significance": event.get("significance", "")
            }
        }
        
        # Добавляем дополнительные атрибуты
        if "end_year" in event:
            node["attributes"]["end_year"] = event["end_year"]
        if "organization" in event:
            node["attributes"]["organization"] = event["organization"]
        if "activity" in event:
            node["attributes"]["activity"] = event["activity"]
        
        graph["nodes"].append(node)
        
        # Связь с Фрадковым
        graph["edges"].append({
            "from": "person_fradkov_pm",
            "to": event_id,
            "type": "participates_in",
            "attributes": {
                "year": event["year"],
                "role": event["type"]
            }
        })
    
    # Сохраняем обогащённый граф
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(graph, f, ensure_ascii=False, indent=2)
    
    print(f"✓ Граф обогащён временной шкалой")
    print(f"  Добавлено событий: {len(timeline['events'])}")
    print(f"  Период: {graph['metadata']['timeline']['date_range']['start']}-{graph['metadata']['timeline']['date_range']['end']}")
    print(f"  Типы событий:")
    for event_type, count in sorted(graph["metadata"]["timeline"]["events_by_type"].items()):
        print(f"    {event_type}: {count}")
    
    return graph


